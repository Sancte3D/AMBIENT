const uint8_t *font_glyph(char c) {
    for (size_t i = 0; i < sizeof(glyphs) / sizeof(glyphs[0]); ++i) {
        if (glyphs[i].c == c) {
            return glyphs[i].rows;
        }
    }
    return NULL;  /* unknown char -> caller renders a blank cell */
}
