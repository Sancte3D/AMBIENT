/*
 * Step-2 starter font.
 *
 * 8x8 cell, drawn as a 5x7 glyph in the top-left (5 cols wide, 7 rows tall),
 * with the right 3 columns and the bottom row left blank for inter-character
 * + inter-line spacing. Each glyph is 8 bytes, one byte per row, top-to-bottom;
 * MSB (bit 7) is the leftmost pixel.
 *
 * Only the characters needed to render "FIELD AMBIENCE V0.9 STEP 2" are
 * present, plus a few extras likely to be used soon (KEY/MODE/PROG/etc.
 * menu labels). Anything else returns NULL from font_glyph() and is rendered
 * as a blank cell — that is by design for "schritt für schritt": future
 * steps extend the glyph table rather than ship a 500-byte font we don't
 * use yet.
 */

